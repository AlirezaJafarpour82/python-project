def AreaOfTri(base,height) :
    '''
    (int,int) -> int
    In this function we will calculate the area of an triangle by having its base and height.

    Examples : 
    AreaOfTri(3,2) ➞ 3 
    AreaOfTri(7,4) ➞ 14 
    AreaOfTri(10,10) ➞ 50
    '''

    area=(base*height)/2
    return area
print(AreaOfTri(1,4))
